# p -> prints to the screen
#   -> returns passed string
#   -> adds new line

str1 = p("printing using p")
p str1

# puts -> prints to the screen
#      -> does not return anything
#      -> adds new line

str1 = puts("printing using puts")
puts str1.class

# print -> prints to the screen
#      -> does not return anything
#      -> does not add new line

str1 = print("printing using p")
print str1.class